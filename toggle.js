function toggleCon() {
  let dg = document.getElementById("Instaid");
  if (dg.style.display === "none") {
    dg.style.display = "block";
  } else {
    dg.style.display = "none"}
}

function toggleMus() {
  let dg = document.getElementById("Showmusic");
  if (dg.style.display === "none") {
    dg.style.display = "block";
  } else {
    dg.style.display = "none"}
}
